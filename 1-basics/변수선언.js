/**
 * 선언과 할당
 * 
 * 선언 = 변수를 선언하는것.
 * 할당 =  선언한 변수에 값을 넣어주는것
 * 
 * 
 * var name ;  이것은 변수를 선언한 것이다.
 * var name = '윈터'  이것을 선언한 변수에 '윈터'라는 값을 할당한것이다.
 * 
 * const는 반드시 값을 초기화 하고 선언해야한다. 
 * 초기화란 선언된 변수에 값을 할당하여 초기값으로 만드는 것을 의미
 * 즉, const는 변수만 선언한 상태 "const name;" 이런식으로 사용 할 수없다.
 */